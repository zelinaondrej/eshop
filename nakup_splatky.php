<h1>Nákup na splátky</h1>

<h3>Společnost Nabah Solutions s.r.o. není vázaným zástupcem. Poskytovatelem úvěru je Home Credit a.s.</h3>

<h2>Jak nakoupit na splátky s vyzvednutím zboží na prodejně?</h2>
<ul>
    <li>Vložte požadované zboží do nákupního košíku</li>
    <li>Způsob dopravy zvolte „osobní odběr“</li>
    <li>Jako platbu vyberte „na splátky“</li>
    <li>Dokončete objednávku a vyčkejte na telefonát prodejce, který s vámi domluví termín schůzky na prodejně, kde splátky sjednáte</li>
    <li>Na prodejně budete potřebovat platný občanský průkaz a údaje o zaměstnavateli (adresa a telefonní číslo na personální/účetní oddělení) – sjednání standardně netrvá déle než 15 minut</li>
</ul>

<h2>Jak nakoupit na splátky s doručením přepravní službou?</h2>

<ul>
    <li>Vložte požadované zboží do nákupního košíku</li>
    <li>Způsob dopravy zvolte „PPL nebo poštou“ (Při nákupu na splátky je možné doručovat pouze PPL nebo poštou.)</li>
    <li>Jako platbu vyberte „na splátky“</li>
    <li>Dokončete objednávku a budete ihned přesměrování na stránky společnosti Home Credit a.s., kde si zvolíte variantu splácení, počet splátek a výši akontace</li>
    <li>V dalším kroku vyplníte do jednoduchého formuláře všechny potřebné údaje pro poskytnutí úvěru a odešlete žádost ke zpracování. Všechny předávané údaje podléhají podmínkám zákona 101/2000 Sb. o ochraně osobních údajů.
Po vyplnění formuláře se dozvíte výsledek schválení, který vám bude zároveň potvrzen na Vaši e-mailovou adresu.V případě schválení obdržíte na svoji e-mailovou adresu smlouvu s návodem, jak dál postupovat.
SMS podpis: Jak na splátky přes mobil?
Ve splátkové kalkulačce Home Credit zvolte „zaslání e-mailem” a „podepsání elektronicky” ve Správci financí jako způsob zaslání dokumentace.
Aplikace vytvoří návrh úvěru, nezapomeňte si jej (ale i další náležitosti, jako číslo účtu nebo prohlášení o pravdivosti údajů) zkontrolovat, než se zeleným tlačítkem posunete k dalším krokům.
Průvodce Vám po odsouhlasení podmínek úvěru nabídne vygenerování SMS s potvrzovacím kódem. Až ji obdržíte, vepište přijatý kód do připraveného políčka ve webovém formuláři a klikněte na tlačítko Podepsat smlouvu a odeslat ke zpracování.
Je třeba zaevidovat Váš doklad totožnosti. Stačí vyfotit či naskenovat občanský či řidičský průkaz a nahrát ho zeleným tlačítkem ze svého počítače či telefonu. Nezapomeňte ověřit, že je doklad čitelný z obou stran.
Tím je z Vaší strany vše dokončeno, stále se můžete ke své podepsané smlouvě vrátit v Digitálním archivu.</li>
</ul>


