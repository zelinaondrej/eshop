<h1 class="reklamace">Reklamační řád a servis</h1>
Jaká máte práva a povinnosti jako zákazník (fyzická osoba) při reklamacích? Doufáme, že to nikdy nebudete muset zjišťovat, náš reklamační řád se snažíme využívat jen výjimečně.

<h2>Reklamační řád</h2>
Všeobecná ustanovení
1.1. Reklamační řád (dále jen „RŘ“) je nedílnou součástí Všeobecných obchodních podmínek (dále jen „VOP“). Povinností kupujícího je seznámit se se zněním RŘ ještě před uskutečněním objednávky Zboží. Provedením objednávky Zboží a následně jeho koupí dochází k uzavření kupní smlouvy mezi Prodávajícím a Kupujícím, mimo jiné tak dochází i k udělení souhlasu Kupujícího s RŘ a potvrzení, že se s ním seznámil.

1.2. Reklamační řád byl zpracován dle ustanovení zákona č. 89/2012 Sb., občanského zákoníku (dále jen „NOZ“) a zákona č. 634/1992 Sb., o ochraně spotřebitele ve znění pozdějších předpisů (dále jen „Zákon“) a vztahuje se na spotřební zboží (dále jen „Zboží“), u něhož jsou v záruční době uplatňována práva Kupujícího z odpovědnosti za vady.

1.3. Zákazníkem prodávajícího Mironet cz, a.s. (dále jen „Prodávající“)  je buď  Kupující-spotřebitel ve smyslu § 2 odst. 1 písm. a) zákona č. 634/1992 Sb. o ochraně spotřebitele (dále jen “Kupující-spotřebitel“) nebo kupující-podnikatel, který při uzavírání a plnění smlouvy jedná v rámci své podnikatelské činnosti (dále jen „Kupující–podnikatel“). Kupující–spotřebitel a Kupující–podnikatel jsou dále společně označováni jako „Kupující“.

1.4. Prodávající při uzavírání a plnění kupní smlouvy jedná v rámci své podnikatelské činnosti. Prodávající je podnikatelem, který přímo nebo prostřednictvím jiných podnikatelů dodává Kupujícímu výrobky nebo služby.


<h2>Odpovědnost prodávajícího</h2>
2.1. Prodávající odpovídá Kupujícímu, že Zboží při převzetí nemá vady. Zejména Prodávající odpovídá, že v době, kdy Kupující Zboží převzal:

- Zboží vyhovuje požadavkům právních předpisů

- Zboží se hodí k účelu, který pro jeho použití Prodávající uvádí nebo ke kterému se věc tohoto druhu obvykle používá

- Zboží má vlastnosti, které si strany ujednaly, a chybí-li ujednání, takové vlastnosti, které Prodávající nebo výrobce popsal nebo které Kupující očekával s ohledem na povahu zboží a na základě reklamy jimi prováděné

- Zboží odpovídá jakostí nebo provedením smluvenému vzorku nebo předloze, byla-li jakost nebo provedení určeno podle smluveného vzorku nebo předlohy

-  Zboží  je  v odpovídajícím množství, míře nebo hmotnosti

2.2. Prodávající ke Zboží přikládá daňový doklad, k některým výrobkům záruční list. Není-li ke Zboží přiložen záruční list, slouží k uplatnění vad Zboží daňový doklad.

2.3. Odpovědnost prodávajícího za vady Zboží se nevztahuje na případ, kdy vada či poškození Zboží vzniklo:

- nesprávným užíváním Zboží nebo jeho užíváním v rozporu s návodem k použití, pokyny uvedenými na obalu nebo v záručním listu Zboží. Užíváním Zboží v rozporu s obecně závaznými pravidly užívání

- nedovolenými zásahy do přístroje nebo jeho mechanickým poškozením. Neodbornou instalací nebo obsluhou Zboží.

- elektrickým přepětím

- užíváním Zboží v podmínkách, které neodpovídá svojí prašností, vlhkostí a teplotou prostředí, které je výrobcem určeno pro užívání Zboží nebo toto jasně vyplývá z povahy samotného Zboží.


<h2>Vady Zboží a odpovědnost za ně</h2>
3.1. Při osobním odběru Kupujícím je okamžik převzetí Zboží okamžikem přechodu nebezpečí škody na Zboží z Prodávajícího na Kupujícího. Kupující si Zboží prohlédne při převzetí. V případě zjištěných vad Zboží má Kupující právo uplatnit zjištěnou vadu u Prodávajícího. Později zjištěná vada Zboží nebrání Kupujícímu uplatnit zjištěnou vadu u Prodávajícího, ale břemeno prokázání, že Zboží mělo tuto vadu již v době přechodu nebezpečí škody z Prodávajícího na Kupujícího nese plně Kupující.

3.2.  Při doručení Zboží dopravcem si Kupující do 48 hodin od převzetí zásilky řádně zkontroluje neporušenost Zboží a kompletnost jeho příslušenství. Poškození Zboží nebo obalu Zboží či vadu Zboží je třeba ihned řešit se zástupcem dopravce prostřednictvím sepsání protokolu, kde jsou uvedeny všechny nesrovnalosti a zjištěné vady Zboží nebo jeho obalu. Kupující nemá povinnost poškozené Zboží od dopravce převzít. O zjištěném poškození bez zbytečného odkladu Kupující informuje Prodávajícího.

3.3. Kupující - spotřebitel má právo uplatnit u Prodávajícího vady Zboží ve smyslu podstatného či nepodstatného vadného plnění z kupní smlouvy v souladu se zákonnou úpravou.

3.4. V případě podstatného vadného plnění z kupní smlouvy má Kupující – spotřebitel nárok na odstranění vadného plnění dodáním bezvadného nového Zboží, dodáním chybějícího Zboží, odstranění vady opravou Zboží, poskytnutím přiměřené slevy z kupní ceny Zboží, právo na odstoupení od kupní smlouvy. Přiměřenou slevou z kupní ceny zboží se rozumí procentuální  snížení hodnoty Zboží v důsledku vady s přihlédnutím k amortizaci a délce užívání Zboží Kupujícím – spotřebitelem.

3.5. V případě nepodstatného vadného plnění z kupní smlouvy má Kupující – spotřebitel nárok na odstranění vady opravou nebo poskytnutí přiměřené slevy z kupní ceny Zboží. Přiměřenou slevou z kupní ceny zboží se rozumí procentuální snížení hodnoty Zboží v důsledku vady s přihlédnutím k amortizaci a délce užívání Zboží Kupujícím – spotřebitelem.

 

<h2>Odstoupení od distanční kupní smlouvy ve lhůtě 14 kalendářních dní</h2>
4.1. Distanční kupní smlouvou se rozumí kupní smlouva, k jejímuž uzavření došlo mezi Kupujícím a Prodávajícím prostřednictvím vzdálených komunikačních prostředků, například prostřednictvím nákupu Zboží v e-shopu Prodávajícího.

4.2. Kupující – spotřebitel má právo odstoupit od distanční kupní smlouvy ve lhůtě 14 kalendářních dní od převzetí Zboží.

4.3. Kupující – spotřebitel je povinen Zboží vrátit Prodávajícímu bez zbytečného odkladu, nejpozději však ve lhůtě 14 kalendářních dní ode dne, kdy sdělil Prodávajícímu svůj záměr na odstoupení od distanční kupní smlouvy. Sdělení musí být provedeno ze strany Kupujícího – spotřebitele prokazatelným způsobem, písemně např. formou elektronické komunikace na e-mailovou adresu Prodávajícího nebo prostřednictvím poštovní zásilky na adresu Prodávajícího. Obsah sdělení musí nesporně prokazovat vůli Kupujícího – spotřebitele na odstoupení od distanční kupní smlouvy.

4.4. Kupující – spotřebitel je povinen volit nejméně nákladný způsob dodání Zboží Prodávajícímu. V takovém případě má Kupující – spotřebitel právo nárokovat si náhradu nákladů řízení dle čl. 4.7.

4.5.  Kupující – spotřebitel, kterému bylo Zboží dodáno prostřednictvím přepravní služby může vratku Zboží realizovat opět prostřednictvím přepravní služby na adrese K Herinku 370, PSČ 251 01, Dobřejovice nebo osobním předáním na kterékoli pobočce (Praha, Brno, Plzeň).

4.6. Prodávající má povinnost Kupujícímu – spotřebiteli vrátit kupní cenu ve lhůtě 14 kalendářních dní od vrácení Zboží. Kupní cena může být Prodávajícím ponížena o částku odpovídající ztrátě, která na Zboží vznikla běžným užíváním. Vrácení kupní ceny probíhá stejným způsobem jako byla Kupujícím – spotřebitelem uhrazena. Jiný způsob vrácení kupní ceny lze provést jen s výslovným souhlasem Kupujícího – spotřebitele.

4.7. Společně s vrácením kupní ceny má Kupující – spotřebitel právo požadovat náhradu nákladů spojených s dodáním Zboží zpět Prodávajícímu. Náhradu těchto nákladů provede Prodávající společně s vrácením kupní ceny dle čl. 4.6.


<h2>Výluky odstoupení od distanční kupní smlouvy ve lhůtě 14 dní</h2>
5.1. Zákon č. 89/2012 Sb. upravuje situace, kdy Kupující – spotřebitel nemá právo na odstoupení od distanční kupní smlouvy.

5.2. Kupující – spotřebitel nemá právo odstoupit o distanční kupní smlouvy v případě, že jejím předmětem je Zboží, které bylo speciálně upraveno na přání a pro potřeby Kupujícího – spotřebitele.

5.3. Kupující – spotřebitel nemá právo odstoupit od distanční kupní smlouvy v případě, že jejím předmětem je Zboží, které podléhá rychlé zkáze nebo bylo po dodání nenávratně smíšeno s jiným Zbožím.

5.4. Kupující – spotřebitel nemá právo odstoupit od distanční kupní smlouvy v případě, že jejím předmětem je Zboží, které bylo vyjmuto z původního obalu a jeho vrácení je nemožné s ohledem na zachování hygienických předpisů a zásad. Například holící a depilační strojky, zubní kartáčky, fitness náramky, hrudní pásy, sluchátka.

5.5. Kupující – spotřebitel nemá právo odstoupit od distanční kupní smlouvy v případě, že jejím předmětem je Zboží, které obsahuje obrazovou nebo zvukovou nahrávku nebo počítačový program a byl porušen jeho původní obal.

5.6. Kupující – spotřebitel nemá právo odstoupit o distanční kupní smlouvy v případě, že jejím předmětem je Zboží digitálního obsahu, který nebyl dodán na hmotném nosiči s předchozím výslovným souhlasem Kupujícího – spotřebitele.


<h2>Reklamace</h2><br>
6.1.  Způsoby předání Zboží k reklamaci

6.1.1. Reklamaci Zboží může Kupující – spotřebitel uplatnit osobně na pobočkách Prodávajícího.

6.1.2. Reklamaci Zboží může Kupující – podnikatel uplatnit výhradně v autorizovaném servisu. Vyřízení reklamace se pak řídí podmínkami a reklamačním řádem autorizovaného servisu. Pro informaci ke konkrétní reklamaci je doporučeno kontaktovat reklamační oddělení.

6.1.3. Reklamaci Zboží zakoupeného se způsobem dodání osobním odběrem na franšízové pobočce Prodávajícího má povinnost Kupující – spotřebitel uplatnit na této pobočce případně v autorizovaném servisu. Důvodem je co největší urychlení reklamačního procesu, kdy jednotlivé franšízové pobočky spolupracují s jednotlivými autorizovanými servisy, vždy ve svém regionu. Pokud by Kupující – spotřebitel uplatnil reklamaci na pobočce Prodávajícího mimo síť franšízových poboček, pak Prodávající zde není v roli osoby povinné k vyřízení reklamace. Na vyřízení reklamace Prodávajícím se pak nevztahují zákonné ani podzákonné předpisy, např. v oblasti běhu lhůt, pro vyřízení reklamace. Prodávající je v takovém případě pouze zprostředkovatelem vyřízení reklamace mezi Kupujícím – spotřebitelem a franšízovou pobočkou. Pro informaci ke konkrétní reklamaci je doporučeno kontaktovat reklamační oddělení.

6.1.4. Kupující - spotřebitel může uplatnit reklamaci Zboží přímo v autorizovaném servisu. Celý reklamační proces je tak zkrácen o roli Prodávajícího jako zprostředkovatele mezi Kupujícím a autorizovaným servisem. Pro informaci ke konkrétní reklamaci je doporučeno kontaktovat reklamační oddělení.

6.1.5.  Reklamaci Zboží lze Prodávajícímu zaslat i prostřednictvím přepravní služby na adresu Mironet.cz a.s. – reklamace, K Herinku 370, Dobřejovice 251 01, případně na partnerské pobočky Prodávajícího. Kupující by měl, ve vlastním zájmu, zabalit reklamované Zboží do vhodného a dostatečně chránícího obalového materiálu vyhovujícího nárokům přepravy tak, aby nedošlo v průběhu přepravy k jeho poškození. U křehkého Zboží by Kupující měl zásilku označit příslušnými symboly. Prodávající doporučuje Kupujícímu přiložit ke Zboží kopii prodejního dokladu, detailní popis reklamované závady a aktuální kontaktní údaje Kupujícího


6.2. Stav Zboží předaného k reklamaci

6.2.1. Kupující je povinen Zboží do reklamace předat čisté a ve stavu, který umožňuje provedení zjištění vad Zboží a případnou opravu.

6.2.2. Právem Prodávajícího je odmítnout převzetí Zboží, které je znečištěné a nebo jsou znečištěny některé jeho části. Nároky na čistotu Zboží se pak liší dle druhu Zboží, s přihlédnutím k obecným hygienickým zásadám a předpisům

6.2.3. Kupující je povinen Zboží do reklamace předat kompletní, včetně jeho součástí a příslušenství.

6.2.4. Právem Prodávajícího je odmítnout převzetí Zboží, které není kompletní a jeho kompletnost je nezbytná ke zjištění vady, která je předmětem reklamace nebo odstranění této vady. Počátek běhu lhůty pro vyřízení reklamace je pak pozastaven do doby, než Kupující dodá zbývající části Zboží, tak aby bylo kompletní.

6.2.5. Prodávající má právo odmítnout převzetí Zboží do reklamace, pokud z něj nejsou beze zbytku odstraněny a vyčištěny zbytky potravy, oleje, benzínu nebo motorového oleje. Stejně má Prodávající právo odmítnout převzetí Zboží do reklamace pokud obsahuje náplně kapalné, plynné či tuhé, které znemožňují další bezpečnou manipulaci se Zbožím.

6.2.6. Kupující je povinen Zboží do reklamace předat po odstranění blokací a zaheslování ( např. FMI, Xiaomi účet, Google účet atd.), sdělí PIN a gesto.

6.2.7. Kupující – spotřebitel je povinen před předáním Zboží do reklamace provést zálohu dat. Prodávající nenese žádnou odpovědnost za případné porušení či ztrátu dat u Zboží, které bylo předáno k reklamaci.

6.2.8. Prodávající doporučuje, aby Kupující - spotřebitel společně se zbožím předaným do reklamace předložil i prodejní doklad, případně dodací list nebo výdejku Zboží, záruční list nebo záruční kartu, certifikát Zboží a další doklady, které byly ke Zboží vystaveny.  

6.2.9. Kupující přiloží ke Zboží popis závady a své aktuální kontaktní údaje v případě, že nebude osobně přítomen vyhotovení reklamačního protokolu.

6.2.10. Náklady vynaložené na uplatnění reklamace jsou hrazeny Kupujícím. V případě uznání reklamace za oprávněnou má Kupující – spotřebitel právo nárokovat úhradu části účelně vynaložených nákladů vynaložených na uplatnění reklamace po Prodávajícím. Povinností Kupujícího spotřebitele je zajištění, aby tyto náklady byly nejnižší možné. Proplacení těchto nákladů ze strany Prodávajícího je podmíněno uplatněním práva Kupujícího formou žádosti bez zbytečného odkladu, nejpozději však ve lhůtě jednoho měsíce od konce lhůty pro uplatnění práv z vadného plnění.

6.2.11. Kupující je povinen poskytnutou Prodávajícímu veškerou součinnost k ověření existence reklamované vady, jako je např. ozkoušení výrobku.

6.2.12. Při převzetí Zboží k reklamaci obdrží Kupující reklamační protokol, který je dokladem platným po celou dobu trvání reklamačního řízení. Obsahem reklamačního protokolu jsou informace o termínu převzetí Zboží, informace o vadě zboží, případně preferenci způsobu vyřízení reklamace ze strany Kupujícího – spotřebitele.

6.2.13. V případě předání Zboží dle čl. 6.1.5. obdrží Kupující reklamační protokol formou e-mailu, po té kdy je Zboží fakticky doručeno přepravní službou Prodávajícímu.

6.2.14. Kupující má povinnost uvést reklamované vady Zboží nejpozději při předání Zboží k vyřízení reklamace, na vady Zboží uplatňované později nebude brán zřetel.


6.3. Oprávněná reklamace

6.3.1. Prodávající po prohlídce Zboží a dokladů předaných společně se Zbožím k reklamaci rozhodne o oprávněnosti reklamace.

6.3.2. Pokud je to možné, oprávněnou reklamaci vyřídí prodávající na místě.

6.3.3. Pokud není možné vyřízení oprávněné reklamace na místě, přijme Prodávající Zboží od Kupujícího – spotřebitele do reklamačního řízení. O převzetí Zboží do reklamačního řízení vystaví Prodávající Kupujícímu – spotřebiteli písemný doklad o převzetí Zboží.

6.3.4. V rámci reklamačního řízení probíhá podrobné odborné posouzení na jehož základě je reklamace uznána za oprávněnou nebo neoprávněnou.


6.4. Neoprávněná reklamace

6.4.1.. Prodávající po prohlídce Zboží a dokladů předených společně se Zbožím k reklamaci rozhodne o neoprávněnosti reklamace. Prodávající vrátí Zboží Kupujícímu.

6.4.2. V případě neoprávněné reklamace Zboží má Prodávající právo uplatnit náhradu nákladů spojených s vyřízením reklamace vůči Kupujícímu – podnikateli.


6.5. Vyřízení reklamace

6.5.1. Reklamace Kupujícího - spotřebitele, včetně odstranění vady, musí být vyřízena bez zbytečného odkladu, nejdéle však ve lhůtě 30 kalendářních dní, pokud nedojde k dohodou mezi Kupujícím a Prodávajícím jinak. Běh lhůty počíná běžet dnem následujícím po předání Zboží Prodávajícímu k reklamaci. Lhůta v 30 kalendářních dní není závazná pro vyřízení reklamace Kupujícího-podnikatele. 

6.5.2. Prodávající provede prohlídku Zboží a dokladů předaných společně se Zbožím k reklamaci a  rozhodne o oprávněnosti  nebo neoprávněnosti reklamace.

6.5.3. O vyřízení reklamace Prodávající informuje Kupujícího – spotřebitele předem dohodnutým způsobem prostřednictvím kontaktních údajů, které mu Kupující – spotřebitel uvedl na reklamačním protokolu. Pokud je Zboží předáno k reklamaci dle čl. 6.1.5,  informace o  vyřízení reklamace  je zaslána na adresu Kupujícího - spotřebitele, kterou uvedl při předání Zboží přepravní službě.

6.5.4. Informace o vyřízení reklamace obsahuje datum a způsob vyřízení reklamace, případně sdělení o zamítnutí reklamace, včetně odůvodnění.

